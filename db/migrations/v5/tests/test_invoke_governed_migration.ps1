    $bad = New-BaseInput "MOCK"
    $bad.schemaDiffSummary = @{ safe = $false }
    $badPath = Join-Path $tmpRoot "bad_schema.json"
    ($bad | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $badPath -Encoding UTF8
    $r = Invoke-Wrapper -InputJsonPath $badPath -OutputDir (Join-Path $tmpRoot "out_bad_schema")
