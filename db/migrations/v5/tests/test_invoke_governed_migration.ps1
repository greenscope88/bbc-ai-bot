    Assert-True ($r.Object.success -eq $true) "[MOCK] expected success=true"
    Assert-True ($r.Object.executed -eq $false) "[MOCK] expected executed=false"
    Assert-True ($r.Object.mode -eq "MOCK") "[MOCK] expected mode=MOCK"
    Assert-True ($r.Object.reportGenerated -eq $true) "[MOCK] expected reportGenerated=true"

    # Ensure report files generated by report_generator
    Assert-True (Test-Path -LiteralPath (Join-Path $mockOut "execution_report.json")) "[MOCK] missing execution_report.json"
    Assert-True (Test-Path -LiteralPath (Join-Path $mockOut "execution_report.md")) "[MOCK] missing execution_report.md"
    Assert-True (Test-Path -LiteralPath (Join-Path $mockOut "risk_summary.md")) "[MOCK] missing risk_summary.md"
    Assert-True (Test-Path -LiteralPath (Join-Path $mockOut "schema_diff_summary.md")) "[MOCK] missing schema_diff_summary.md"
    Assert-True (Test-Path -LiteralPath (Join-Path $mockOut "recovery_readiness_summary.md")) "[MOCK] missing recovery_readiness_summary.md"

    # Case 2: DRY_RUN success
    $dryInputPath = Join-Path $tmpRoot "dry_input.json"
    (New-BaseInput "DRY_RUN" | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $dryInputPath -Encoding UTF8
    $dryOut = Join-Path $tmpRoot "out_dry"
    $r = Invoke-Wrapper -InputJsonPath $dryInputPath -OutputDir $dryOut
    Assert-True ($r.ExitCode -eq 0) "[DRY_RUN] expected exit 0"
    Assert-True ($r.Object.success -eq $true) "[DRY_RUN] expected success=true"
    Assert-True ($r.Object.mode -eq "DRY_RUN") "[DRY_RUN] expected mode=DRY_RUN"

    # Case 3: LIVE_EXECUTE must fail
    $liveInputPath = Join-Path $tmpRoot "live_input.json"
    (New-BaseInput "LIVE_EXECUTE" | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $liveInputPath -Encoding UTF8
    $r = Invoke-Wrapper -InputJsonPath $liveInputPath -OutputDir (Join-Path $tmpRoot "out_live")
    Assert-True ($r.ExitCode -eq 1) "[LIVE] expected exit 1"
    Assert-True ($r.Object.success -eq $false) "[LIVE] expected success=false"
    Assert-True ($r.Object.reason -eq "LIVE_EXECUTE is not supported in this phase") "[LIVE] expected reason"

    # Case 4: approval not approved -> fail
    $bad = New-BaseInput "MOCK"
    $bad.approval = @{ approved = $false }
    $badPath = Join-Path $tmpRoot "bad_approval.json"
    ($bad | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $badPath -Encoding UTF8
    $r = Invoke-Wrapper -InputJsonPath $badPath -OutputDir (Join-Path $tmpRoot "out_bad_approval")
    Assert-True ($r.ExitCode -eq 1) "[APPROVAL] expected exit 1"
    Assert-True ($r.Object.success -eq $false) "[APPROVAL] expected success=false"

    # Case 5: recoveryReadiness not ready -> fail
    $bad = New-BaseInput "MOCK"
    $bad.recoveryReadiness = @{ ready = $false }
    $badPath = Join-Path $tmpRoot "bad_recovery.json"
    ($bad | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $badPath -Encoding UTF8
    $r = Invoke-Wrapper -InputJsonPath $badPath -OutputDir (Join-Path $tmpRoot "out_bad_recovery")
    Assert-True ($r.ExitCode -eq 1) "[RECOVERY] expected exit 1"
    Assert-True ($r.Object.success -eq $false) "[RECOVERY] expected success=false"

    # Case 6: riskSummary not allowed -> fail
    $bad = New-BaseInput "MOCK"
    $bad.riskSummary = @{ allowed = $false }
    $badPath = Join-Path $tmpRoot "bad_risk.json"
    ($bad | ConvertTo-Json -Depth 20) | Set-Content -LiteralPath $badPath -Encoding UTF8
    $r = Invoke-Wrapper -InputJsonPath $badPath -OutputDir (Join-Path $tmpRoot "out_bad_risk")
    Assert-True ($r.ExitCode -eq 1) "[RISK] expected exit 1"
    Assert-True ($r.Object.success -eq $false) "[RISK] expected success=false"

