    Assert-True ($r.Object.success -eq $false) "[SCHEMA] expected success=false"

    # Case 8: report files generated already checked in success path
    # Case 9: no dependency on tenant_service_limits.sql
    Assert-True ($r.Raw -notmatch "tenant_service_limits") "[NO_TSL_DEP] unexpected tenant_service_limits mention"

    Write-Output "PASS: test_invoke_governed_migration.ps1"
}
finally {
    # Keep temp artifacts for post-run inspection if needed.
    Write-Output "INFO: temp files kept at $tmpRoot"
}

